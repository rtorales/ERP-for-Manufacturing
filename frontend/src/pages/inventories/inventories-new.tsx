import { create } from '../../stores/inventories/inventoriesSlice';
import { useAppDispatch } from '../../stores/hooks';
import { useRouter } from 'next/router';

const TablesPage = () => {
  const router = useRouter();
  const dispatch = useAppDispatch();

  const handleSubmit = async (data) => {
    await dispatch(create(data));
    await router.push('/inventories/inventories-list');
  };
  return (
    <>
      <Head>
        <title>{getPageTitle('New Item')}</title>
      </Head>
      <SectionMain>
        <SectionTitleLineWithButton
          icon={mdiChartTimelineVariant}
          title='New Item'
          main
        >
          Breadcrumbs
        </SectionTitleLineWithButton>
        <CardBox>
          <Formik
            initialValues={{
              product_name: '',

              available_quantity: '',

              reserved_quantity: '',

              returned_quantity: '',
            }}
            onSubmit={(values) => handleSubmit(values)}
          >
            <Form>
              <FormField label='ProductName'>
                <Field name='product_name' placeholder='Your ProductName' />
              </FormField>

              <FormField label='AvailableQuantity'>
                <Field
                  type='number'
                  name='available_quantity'
                  placeholder='Your AvailableQuantity'
                />
              </FormField>

              <FormField label='ReservedQuantity'>
                <Field
                  type='number'
                  name='reserved_quantity'
                  placeholder='Your ReservedQuantity'
                />
              </FormField>

              <FormField label='ReturnedQuantity'>
                <Field
                  type='number'
                  name='returned_quantity'
                  placeholder='Your ReturnedQuantity'
                />
              </FormField>

              <BaseDivider />
              <BaseButtons>
                <BaseButton type='submit' color='info' label='Submit' />
                <BaseButton type='reset' color='info' outline label='Reset' />
                <BaseButton
                  type='reset'
                  color='danger'
                  outline
                  label='Cancel'
                  onClick={() => router.push('/inventories/inventories-list')}
                />
              </BaseButtons>
            </Form>
          </Formik>
        </CardBox>
      </SectionMain>
    </>
  );
};

TablesPage.getLayout = function getLayout(page: ReactElement) {
  return (
    <LayoutAuthenticated permission={'CREATE_INVENTORIES'}>
      {page}
    </LayoutAuthenticated>
  );
};

export default TablesPage;
