/**
 *  @swagger
 *  /api/raw_materials:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Raw_materials]
 *      summary: Get all raw_materials
 *      description: Get all raw_materials
 *      responses:
 *        200:
 *          description: Raw_materials list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Raw_materials"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */

router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;
    const payload = await Raw_materialsDBApi.findAll(req.query);
    if (filetype && filetype === 'csv') {
      const fields = ['id', 'material_name', 'stock_quantity', 'reorder_level'];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

