/**
 *  @swagger
 *  /api/inventories:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Inventories]
 *      summary: Get all inventories
 *      description: Get all inventories
 *      responses:
 *        200:
 *          description: Inventories list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Inventories"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */

router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;
    const payload = await InventoriesDBApi.findAll(req.query);
    if (filetype && filetype === 'csv') {
      const fields = [
        'id',
        'product_name',
        'available_quantity',
        'reserved_quantity',
        'returned_quantity',
      ];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

